#include "ft_rgyles.h"

int
	ft_num_len(int n)
{
	int i;
	int nbr;

	if (n == 0)
		return  (1);
	nbr = n;
	i = 0;
	while (nbr > 0)
	{
		i++;
		nbr /= 10;
	}
	return (i);
}
